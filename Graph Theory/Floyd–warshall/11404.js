let [[n], [], ...I] = require('fs')
  .readFileSync('./dev/stdin')
  .toString()
  .trim()
  .split('\r\n')
  .map(e => e.split(' ').map(Number))
// let I = require('fs').readFileSync('/dev/stdin').toString().trim().split('\n').map(e => e.split(' ').map(Number))
const O = [...Array(n)].map(_ => Array(n).fill(Infinity))
for (let i = 0; i < n; i++) O[i][i] = 0
I.forEach(([a, b, c]) => (O[--a][--b] = Math.min(O[a][b], c)))
for (let i = 0; i < n; i++)
  for (let j = 0; j < n; j++)
    for (let k = 0; k < n; k++) O[j][k] = Math.min(O[j][k], O[j][i] + O[i][k])
console.log(O.map(e => e.join(' ').replaceAll('Infinity', 0)).join('\n'))
