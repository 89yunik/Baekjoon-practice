let [[, K], ...I] = require('fs')
  .readFileSync('./dev/stdin')
  .toString()
  .trim()
  .split('\r\n')
  .map(e => e.split(' ').map(Number))
// let [[N, K], ...I] = require('fs').readFileSync('/dev/stdin').toString().trim().split('\n').map(e => e.split(' ').map(Number))
let dp = Array(K + 1).fill(0)
for (const [W, V] of I)
  for (let j = K; j >= W; j--) dp[j] = Math.max(dp[j], dp[j - W] + V)
console.log(dp[K])
